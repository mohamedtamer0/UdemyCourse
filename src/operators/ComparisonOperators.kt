Comparison and Equality Operators
    Operator	    Meaning	                      Expression
    >	          greater than	                    a > b
    <	           less than	                     a < b
    >=	      greater than or equals to	            a >= b
    <=	       less than or equals to	            a < = b
    ==	        is equal to	                        a == b
    !=	         not equal to	                    a != b
 */
